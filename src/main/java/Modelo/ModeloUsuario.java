package Modelo;

public class ModeloUsuario {

    private String usuario;
    private String clave;

    public ModeloUsuario(String usuario, String clave) {
        this.usuario = usuario;
        this.clave = clave;
    }

    public String getUsuario() {
        return usuario;
    }

    public String getClave() {
        return clave;
    }
}
